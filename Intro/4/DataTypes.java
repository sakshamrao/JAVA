public class DataTypes
{
	public static void main(String [] args)
	{
	System.out.println("size of BYTE : 1 bytes" + "range of BYTE : -128 to +127");
	System.out.println("size of SHORT : 2 bytes" + "range of SHORT : -32,768 to +32,767");
	System.out.println("size of INT : 4 bytes" + "range of INT : -2,147,483,648 to +2,127,483,647");
	System.out.println("size of LONG : 8 bytes" + "range of LONG : -9,223,372,036,854,775,808 to +9,223,372,036,854,775,807");
	System.out.println("size of CHAR : 2 bytes" + "range of CHAR : 0 to 65,536");
	System.out.println("size of FLOAT : 4 bytes" + "range of FLOAT : -3.4E+38 to +3.4E+38");
	System.out.println("size of DOUBLE : 8 bytes" + "range of DOUBLE : -1.7E+308 to +1.7E+308");
	System.out.println("size of BOOLEAN : not defined" + "range of BOOLEAN : true or false");
	}
}